package com.ay.proyectopetisos.Model;

public class Canes {
    private int idCategoria;
    private int idAlbergue;
    private int idCanino;
    private String nombreCanino;
    private String historiaCanino;
    private String imgCanino;
    private String sexoCanino;
    private String edadCanino;
    private String razaCanino;
    private String estadoCanino;

    public Canes() {
        this.idCategoria = idCategoria;
        this.idAlbergue = idAlbergue;
        this.idCanino = idCanino;
        this.nombreCanino = nombreCanino;
        this.historiaCanino = historiaCanino;
        this.imgCanino = imgCanino;
        this.sexoCanino = sexoCanino;
        this.edadCanino = edadCanino;
        this.razaCanino = razaCanino;
        this.estadoCanino = estadoCanino;
    }

    public String getSexoCanino() {
        return sexoCanino;
    }

    public void setSexoCanino(String sexoCanino) {
        this.sexoCanino = sexoCanino;
    }

    public String getEdadCanino() {
        return edadCanino;
    }

    public void setEdadCanino(String edadCanino) {
        this.edadCanino = edadCanino;
    }

    public String getRazaCanino() {
        return razaCanino;
    }

    public void setRazaCanino(String razaCanino) {
        this.razaCanino = razaCanino;
    }

    public String getEstadoCanino() {
        return estadoCanino;
    }

    public void setEstadoCanino(String estadoCanino) {
        this.estadoCanino = estadoCanino;
    }

    public int getIdCategoria() {
        return idCategoria;
    }

    public void setIdCategoria(int idCategoria) {
        this.idCategoria = idCategoria;
    }

    public int getIdAlbergue() {
        return idAlbergue;
    }

    public void setIdAlbergue(int idAlbergue) {
        this.idAlbergue = idAlbergue;
    }

    public int getIdCanino() {
        return idCanino;
    }

    public void setIdCanino(int idCanino) {
        this.idCanino = idCanino;
    }

    public String getNombreCanino() {
        return nombreCanino;
    }

    public void setNombreCanino(String nombreCanino) {
        this.nombreCanino = nombreCanino;
    }

    public String getHistoriaCanino() {
        return historiaCanino;
    }

    public void setHistoriaCanino(String historiaCanino) {
        this.historiaCanino = historiaCanino;
    }

    public String getImgCanino() {
        return imgCanino;
    }

    public void setImgCanino(String imgCanino) {
        this.imgCanino = imgCanino;
    }
}
