package com.ay.proyectopetisosalbergue.Model;

public class Consejos {
    private String nomAlbergue;
    private String icAlbergue;
    private String imgConsejo;
    private String tituloConsejo;
    private String descConsejo;
    private String vinculoConsejo;

    public Consejos() {
        this.nomAlbergue = nomAlbergue;
        this.icAlbergue = icAlbergue;
        this.imgConsejo = imgConsejo;
        this.tituloConsejo = tituloConsejo;
        this.descConsejo = descConsejo;
        this.vinculoConsejo = vinculoConsejo;
    }

    public String getNomAlbergue() {
        return nomAlbergue;
    }

    public void setNomAlbergue(String nomAlbergue) {
        this.nomAlbergue = nomAlbergue;
    }

    public String getIcAlbergue() {
        return icAlbergue;
    }

    public void setIcAlbergue(String icAlbergue) {
        this.icAlbergue = icAlbergue;
    }

    public String getImgConsejo() {
        return imgConsejo;
    }

    public void setImgConsejo(String imgConsejo) {
        this.imgConsejo = imgConsejo;
    }

    public String getTituloConsejo() {
        return tituloConsejo;
    }

    public void setTituloConsejo(String tituloConsejo) {
        this.tituloConsejo = tituloConsejo;
    }

    public String getDescConsejo() {
        return descConsejo;
    }

    public void setDescConsejo(String descConsejo) {
        this.descConsejo = descConsejo;
    }

    public String getVinculoConsejo() {
        return vinculoConsejo;
    }

    public void setVinculoConsejo(String vinculoConsejo) {
        this.vinculoConsejo = vinculoConsejo;
    }
}
